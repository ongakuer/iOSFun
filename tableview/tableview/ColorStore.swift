import UIkit

class ColorStore{
    
    class func purpleDark() -> UIColor! {
        return UIColor(red: 91/255, green: 38/255, blue: 128/255, alpha: 1)
    }
    
    class func purpleLight() -> UIColor! {
        return UIColor(red: 167/255, green: 103/255, blue: 213/255, alpha: 1)
    }
    
    class func purpleLightFade() -> UIColor! {
        return UIColor(red: 167/255, green: 103/255, blue: 213/255, alpha: 0.5)
    }


}